    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        // TODO add your handling code here:
        try{
        String IDandLINK=jTextArea1.getText();
        if(IDandLINK.equals("")){
            JOptionPane.showMessageDialog(null,"No data available to write in a file");            
        }else{            
            JFileChooser jfc1=new JFileChooser();
            jfc1.showSaveDialog(this);
            File f=jfc1.getSelectedFile();
            String save_path=f.getAbsolutePath();            
            
            File ff;
            if(save_path.endsWith(".xml") || save_path.endsWith(".XML")){                
                ff=new File(save_path);
                FileWriter fwr=new FileWriter(ff);
                    String[] get_dd=IDandLINK.split("\n");
                    String furthersplit;
                    String[] separating;
                    String rightid;
                    String rightlink;

                    DocumentBuilderFactory df=DocumentBuilderFactory.newInstance();
                    DocumentBuilder db= df.newDocumentBuilder();

                    org.w3c.dom.Document d=db.newDocument();

                    org.w3c.dom.Element mainel=d.createElement("webcrawler");
                    d.appendChild(mainel);
                    if(ff.canWrite()){
                    for(int a=0;a<get_dd.length;a++){
                        furthersplit=get_dd[a];
                        separating=furthersplit.split("\t");
                        rightid=separating[0];
                        rightlink=separating[1];

                        org.w3c.dom.Element webpages=d.createElement("page");
                        mainel.appendChild(webpages);

                        org.w3c.dom.Element id=d.createElement("ID");
                        id.appendChild(d.createTextNode(rightid));
                        webpages.appendChild(id);

                        org.w3c.dom.Element link=d.createElement("LINK");
                        link.appendChild(d.createTextNode(rightlink));
                        webpages.appendChild(link);

                        TransformerFactory tf=TransformerFactory.newInstance();
                        Transformer t=tf.newTransformer();
                        t.setOutputProperty(OutputKeys.INDENT, "yes");
                        DOMSource sourdm=new DOMSource(d);                                   
                        StreamResult sr=new StreamResult(ff);                        
                        t.transform(sourdm, sr);            
                    }              
                    JOptionPane.showMessageDialog(null,"File named as "+ff.getName()+" saved to "+save_path);                        
                    }else{
                        JOptionPane.showMessageDialog(null,"File cannot be written");                                                
                    }
            }else{
                JOptionPane.showMessageDialog(null,"Only .xml or .XML format allowed");
            }
        }        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());            
        }

    }//GEN-LAST:event_jMenuItem5ActionPerformed

