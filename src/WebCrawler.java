    private void jMenuItem7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem7ActionPerformed
        // TODO add your handling code here:        
        try{
        String text_to_write=jTextArea1.getText();
        if(text_to_write.equals("")){
            JOptionPane.showMessageDialog(null,"No text available to write in a file");            
        }else{
            JFileChooser jfc1=new JFileChooser();
            jfc1.showSaveDialog(this);
            File file_path=jfc1.getSelectedFile();
            String save_path=file_path.getAbsolutePath();            
            File f2=new File(save_path);
            FileWriter fwr=new FileWriter(f2);
            BufferedWriter bwr=new BufferedWriter(fwr);
            if(f2.canWrite()){
                bwr.write(text_to_write);
                bwr.newLine();
                bwr.close();
                fwr.close();
                JOptionPane.showMessageDialog(null,"File saved to "+save_path);                        
            }else{
                JOptionPane.showMessageDialog(null,"The specified file cannot be written");
            }
            if(f2.isFile()){                    
                if(save_path.endsWith(".txt") || save_path.endsWith(".TXT")){                        
                    jTextField3.setText("File named as "+file_path.getName()+" saved");
                }else{
                    JOptionPane.showMessageDialog(null,"Incorrect file format. Only txt allowed");
                }
            }            
        }        
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());            
        }        
    }//GEN-LAST:event_jMenuItem7ActionPerformed

